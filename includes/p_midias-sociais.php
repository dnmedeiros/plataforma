          <h2 class="featurette-heading">Redes sociais <span class="text-muted">na internet</span></h2>
          <p class="lead">Alguns sites possuem funcionalidades que favorecem a conexão entre atores e tornam evidentes as redes. Essas podem ser chamadas de redes sociais na internet. Raquel Recuero (2009, p. 102) resgata uma definição Boyd & Ellison (2007) que caracteriza sites de redes sociais como sistemas que permitem “i) a construção de uma persona através de um perfil ou página pessoal; ii) a interação através de comentários; e iii) a exposição pública da rede social de cada ator”. <a href="http://ainfo.cnptia.embrapa.br/digital/bitstream/item/104529/1/Planejamento-de-acoes-corporativas-em-midias-sociais-conceitos-e-praticas.pdf" target="_blank">(MEDEIROS, 2013)</a></p>

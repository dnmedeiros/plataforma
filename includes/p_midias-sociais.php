          <h2 class="featurette-heading">A rede e<span class="text-muted"> a plataforma</span></h2>
          <p class="lead">"Orkut não é uma rede social, mas sim uma plataforma de rede social. A rede social se forma sobre a plataforma e pode até estar sobre outras plataformas também. Assim, uma pessoa ou empresa pode ter uma rede social em que seus membros estejam tanto no Orkut quanto no Facebook e no Twitter. Outro exemplo é o próprio Twitter. Ele não é estratégia nem rede social, mas apenas uma plataforma, e o modo como é usado determina as estratégias e a rede social que se forma nessa plataforma." (GABRIEL, 2010, p. 107).</p>

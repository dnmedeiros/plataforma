          <h2 class="featurette-heading">Rede social <span class="text-muted">e mídia social</span></h2>
          <p class="lead">O próprio termo rede social é muitas vezes utilizado com sinônimo de mídia social. Uma análise cuidadosa, no entanto, mostra que são conceitos distintos. Rede social existe desde antes do advento da internet e é definida como “conjunto de dois elementos: atores (pessoas, instituições ou grupos; os nós da rede) e suas conexões (interações ou laços sociais)” <a href="http://midiadigital.jor.br/esamaz/documentos/redessociaisnainternetrecuero.pdf" target="_blank">(WASSERMAN & FAUST, 1994; DEGENNE & FORSE, 1999, apud RECUERO, 2009, P. 24)</a>. </p>
